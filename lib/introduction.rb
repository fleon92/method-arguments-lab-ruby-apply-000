# The method below is programmed to accept an argument and insert it into the string with the #{} syntax.
def introduction(name)
  puts "Hi, my name is #{name}."
end

# The method below is programmed to accept TWO arguments and insert it into the string with the #{} syntax.
def introduction_with_language(name, language)
  puts "Hi, my name is #{name} and I am learning to program in #{language}."
end